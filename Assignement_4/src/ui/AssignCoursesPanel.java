    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (clTable.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row!!");
        } else {
            String courseId = (String) clTable.getValueAt(clTable.getSelectedRow(), 0);
            CourseOffer co = schedule.getCourseOfferByNumber(courseId);
            co.AssignAsTeacher(computerScience.getFacultydirectory().findTeachingFaculty(p.getPersonId()));
            JOptionPane.showMessageDialog(this, "Successfully Assigned for " + co.getCourseNumber());
            populateTable();
            populateTable2();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

