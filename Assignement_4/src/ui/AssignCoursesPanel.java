    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) clTable.getModel();
        dtm.setRowCount(0);
        Object obj[] = new Object[4];
        for (Course c : computerScience.getCourseCatalog().getCourseList()) {
            CourseOffer co = schedule.getCourseOfferByNumber(c.getCourseNumber());
            if (co.getFacultyProfile() == null) {
                obj[0] = c.getCourseNumber();
                obj[1] = c.getName();
                obj[2] = c.getCredits();
                obj[3] = co.getNumOfSeats() - co.getEmptySeats();
                dtm.addRow(obj);
            }
        }
    }

    private void populateTable2() {

        DefaultTableModel dtm = (DefaultTableModel) rcTable.getModel();
        dtm.setRowCount(0);
        Object obj[] = new Object[4];
        for (Course c : computerScience.getCourseCatalog().getCourseList()) {
            CourseOffer co = schedule.getCourseOfferByNumber(c.getCourseNumber());
            if (co.getFacultyProfile() != null && co.getFacultyProfile().getFacultyId().getPersonId().equals(p.getPersonId())) {
                obj[0] = c.getCourseNumber();
                obj[1] = c.getName();
                obj[2] = c.getCredits();
                obj[3] = co.getNumOfSeats() - co.getEmptySeats();
                dtm.addRow(obj);
            }
        }
    }

